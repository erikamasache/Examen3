    public int res(String partido) {
        int resultado = 0;
        if (partido.equalsIgnoreCase("Ganó")) {
            resultado = 3;
        } else {
            if (partido.equalsIgnoreCase("Empató")) {
                resultado = 2;
            } else {
                if (partido.equalsIgnoreCase("Perdió")) {
                    resultado = 1;
                }
            }
        }
        return resultado;
    }

    public String resultado(int partido) {
        String resultado = "";
        if (partido == 3) {
            resultado = "Ganó";
        } else {
            if (partido == 2) {
                resultado = "Empató";
            } else {
                if (partido == 1) {
                    resultado = "Perdió";
                }
            }
        }
        return resultado;
    }

    public void cargarLista() {
        List<Equipo> ls = new ArrayList<Equipo>();
        DaoEquipo daoEquipo = new DaoEquipo();

        ls = daoEquipo.listaEquipo(false);
        DefaultListModel modelo = new DefaultListModel();

        for (Equipo equipo : ls) {
            modelo.addElement(equipo.getNombrepais());
        }

        jlistEquipos.setModel(modelo);

    }
    private void escogerEquipo(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_escogerEquipo
        // TODO add your handling code here:
        ocultarTexto();
        String nombre = (String) jlistEquipos.getSelectedValue();
        List<Equipo> ls = new ArrayList<Equipo>();
        DaoEquipo daoEquipo = new DaoEquipo();
        Equipo equipoSeleccionado = new Equipo();
        ls = daoEquipo.listaEquipo(false);
        for (Equipo equipo : ls) {
            if (equipo.getNombrepais().equals(nombre)) {
                equipoSeleccionado = equipo;
            }
        }
        txtCopas.setText(equipoSeleccionado.getNumerocopasganadas().toString());
        txtId.setText(Integer.toString(equipoSeleccionado.getEquipo()));
        txtPais.setText(equipoSeleccionado.getNombrepais());
        txtRanking.setText(equipoSeleccionado.getRankingfifa().toString());

        txtp1.setText(resultado(equipoSeleccionado.getPartidouno()));

        txtp2.setText(resultado(equipoSeleccionado.getPartidodos()));
        txtp3.setText(resultado(equipoSeleccionado.getPartidotres()));
        cargarComboUno(equipoSeleccionado.getPartidouno());
        cargarComboDos(equipoSeleccionado.getPartidodos());
        cargarComboTres(equipoSeleccionado.getPartidotres());
        txtGrupo.setText(equipoSeleccionado.getGrupo());
            // JOptionPane.showMessageDialog(this, jlistEquipos.getSelectedIndex()+" "+jlistEquipos.getSelectedValue());


    }//GEN-LAST:event_escogerEquipo

