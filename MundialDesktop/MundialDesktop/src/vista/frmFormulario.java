    private void btnGuardar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardar1ActionPerformed
        // TODO add your handling code here:

        if (txtCopas.getText().equals("") || txtGrupo.getText().equals("") || txtPais.getText().equals("") || txtRanking.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campos Vacios, no se puede guardar");

        } else {
            if (txtId.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Los datos no pertenecen a ningún Equipo de la Base de Datos");
            } else {
                Equipo equipo = new Equipo();
                DaoEquipo daoEquipo = new DaoEquipo();
                equipo.setEquipo(Integer.parseInt(txtId.getText()));
                equipo.setNombrepais(txtPais.getText());
                equipo.setPartidodos(res(txtp2.getText()));
                equipo.setPartidouno(res(txtp1.getText()));
                equipo.setPartidotres(res(txtp3.getText()));
                equipo.setNumerocopasganadas(Integer.parseInt(txtCopas.getText()));
                equipo.setRankingfifa(Integer.parseInt(txtRanking.getText()));
                equipo.setGrupo(txtGrupo.getText());
                daoEquipo.editar(equipo);
                JOptionPane.showMessageDialog(this, "Datos guardados");
                cargarLista();
                limpiarTexto();
                ocultarTexto();
            }
        }
    }//GEN-LAST:event_btnGuardar1ActionPerformed

